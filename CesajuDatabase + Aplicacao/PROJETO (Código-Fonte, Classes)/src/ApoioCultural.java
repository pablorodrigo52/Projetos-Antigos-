public class ApoioCultural {
    
    private int num_cadastro;
    private String nome_Membro;
    private String descri_apoio_cultural;
    private String nome_Evento;
    private String local_Evento;
    
    
    
    public int getNum_Cadastro(){
        return num_cadastro;
    }
    public void setNum_Cadastro(int num_cadastro){
        this.num_cadastro = num_cadastro;
    }
    
    
    public String getNome_Membro(){
        return nome_Membro;
    }
    public void setNome_Membro(String nome_Membro){
        this.nome_Membro = nome_Membro;
    }
    
    
    public String getDescri_apoio_cultural(){
        return descri_apoio_cultural;
    }
    public void setDescri_apoio_cultural(String descri_apoio_cultural){
        this.descri_apoio_cultural = descri_apoio_cultural;
    }
    
    
    public String getNome_Evento(){
        return nome_Evento;
    }
    public void setNome_Evento(String nome_Evento){
        this.nome_Evento = nome_Evento;
    }
    
    
    public String getLocal_Evento(){
        return local_Evento;
    }
    public void setLocal_Evento(String local_Evento){
        this.local_Evento = local_Evento;
    }
    
    
}
